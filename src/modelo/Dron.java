package modelo;



public class Dron {
	
	private int x = 0;
	private int y = 0;
	private String Orientacion;
	
	
	
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public String getOrientacion() {
		return Orientacion;
	}
	public void setOrientacion(String orientacion) {
		Orientacion = orientacion;
	}
	
	
}
